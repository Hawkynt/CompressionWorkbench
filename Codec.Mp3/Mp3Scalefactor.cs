  /// <summary>2^(x/4) helper used by the scalefactor dequantizer.</summary>
  public static float LdexpQ2(float y, int expQ2) {
    // mantissa table for 1/4-exponent steps
    ReadOnlySpan<float> frac = stackalloc float[] {
      9.31322575e-10f, 7.83145814e-10f, 6.58544508e-10f, 5.53767716e-10f
    };
    while (expQ2 > 0) {
      var e = expQ2 < 120 ? expQ2 : 120;
      y *= frac[e & 3] * (1 << (30 - (e >> 2)));
      expQ2 -= e;
    }
    return y;
  }
