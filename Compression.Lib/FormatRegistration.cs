using Compression.Registry;

namespace Compression.Lib;

/// <summary>
/// Registers all format descriptors with the central registry.
/// The <c>RegisterFormats()</c> partial method is generated at compile time by
/// <c>Compression.Registry.Generator</c>, which discovers all public classes implementing
/// <c>IFormatDescriptor</c> with a parameterless constructor across referenced assemblies.
/// Compound tar descriptors are registered manually since they are composites, not standalone formats.
/// </summary>
public static partial class FormatRegistration {
  private static bool _done;

  public static void EnsureInitialized() {
    if (_done) return;
    _done = true;
    RegisterFormats();
    RegisterCompoundTar();
    RegisterBuildingBlocks();
    FormatRegistry.Initialize();
  }

  /// <summary>
  /// Generated by Compression.Registry.Generator — calls FormatRegistry.Register()
  /// for every discovered format descriptor. Falls back to no-op if generator is not wired up.
  /// </summary>
  static partial void RegisterFormats();

  /// <summary>
  /// Generated by Compression.Registry.Generator — calls BuildingBlockRegistry.Register()
  /// for every discovered building block. Falls back to no-op if generator is not wired up.
  /// </summary>
  static partial void RegisterBuildingBlocks();

  private static void RegisterCompoundTar() {
    FormatRegistry.Register(new CompoundTarDescriptor("TarGz", "tar.gz", "Gzip", ".tar.gz", [".tar.gz", ".tgz"]));
    FormatRegistry.Register(new CompoundTarDescriptor("TarBz2", "tar.bz2", "Bzip2", ".tar.bz2", [".tar.bz2", ".tbz2"]));
    FormatRegistry.Register(new CompoundTarDescriptor("TarXz", "tar.xz", "Xz", ".tar.xz", [".tar.xz", ".txz"]));
    FormatRegistry.Register(new CompoundTarDescriptor("TarZst", "tar.zst", "Zstd", ".tar.zst", [".tar.zst"]));
    FormatRegistry.Register(new CompoundTarDescriptor("TarLz4", "tar.lz4", "Lz4", ".tar.lz4", [".tar.lz4"]));
    FormatRegistry.Register(new CompoundTarDescriptor("TarLzip", "tar.lz", "Lzip", ".tar.lz", [".tar.lz", ".tlz"]));
    FormatRegistry.Register(new CompoundTarDescriptor("TarBr", "tar.br", "Brotli", ".tar.br", [".tar.br", ".tbr"]));
  }
}
